    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        EndUser student = new EndUserProxy();

        if (!(cmbSnack.getSelectedItem().toString().equals(NONE_SELECTED)) || !(cmbSize.getSelectedItem().toString().equals(NONE_SELECTED)))
        {
            if (!cmbSnack.getSelectedItem().toString().equals(NONE_SELECTED))
            {
                ArrayList<String> addon = new ArrayList<String>();
                String product = cmbSnack.getSelectedItem().toString().replaceAll("\\s+","");
                addon.add(cmbLettuce.getSelectedItem().toString().replaceAll("\\s+",""));
                addon.add(cmbOnion.getSelectedItem().toString().replaceAll("\\s+",""));
                addon.add(cmbTuna.getSelectedItem().toString().replaceAll("\\s+",""));
                addon.add(cmbCheese.getSelectedItem().toString().replaceAll("\\s+",""));
                student.order(ProductCategory.Snack.toString(), product, addon);
            }
            
            if (!cmbSize.getSelectedItem().toString().equals(NONE_SELECTED))
            {
                ArrayList<String> addon = new ArrayList<String>();
                String product = DrinksCategory.Coffee.toString().replaceAll("\\s+","");
                addon.add(cmbTopping.getSelectedItem().toString().replaceAll("\\s+",""));
                addon.add(cmbIce.getSelectedItem().toString().replaceAll("\\s+",""));
                addon.add(cmbMilk.getSelectedItem().toString().replaceAll("\\s+",""));
                addon.add(cmbSugar.getSelectedItem().toString().replaceAll("\\s+",""));
                addon.add(cmbSize.getSelectedItem().toString().replaceAll("\\s+",""));
                student.order(ProductCategory.Drinks.toString(), product, addon);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Invalid order! Please select a drink or a snack!");
        }
    }//GEN-LAST:event_btnOrderActionPerformed

