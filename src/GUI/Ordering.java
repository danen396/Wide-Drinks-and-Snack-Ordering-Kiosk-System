    public Ordering() {
        initComponents();
        
        EndUser endUser = new EndUserProxy();
        ArrayList availableProducts =  endUser.getAvailableProducts();    
        
        for (Object obj : availableProducts)
        {
            String itemName = (String) obj;
            
            if (itemName.equals(DrinksCategory.Coffee.toString()))
            {
                cmbSize.addItem(Size.Regular.toString());
                cmbSize.addItem(Size.Medium.toString());
                cmbSize.addItem(Size.Large.toString());
            }
            else if ((itemName.equals(SnackCategory.CurryPuff.toString()))
                    || (itemName.equals(SnackCategory.Kuih.toString()))
                    || (itemName.equals(SnackCategory.Pau.toString()))
                    || (itemName.equals(SnackCategory.Sandwich.toString())))
            {
                cmbSnack.addItem(itemName);
            }
        }
    }

